function doubler(numbers) {
    return numbers.map(num => num * 2);
  }


//  The map method creates a new array by calling a provided function on each element of the original array and returning the result. In this case, the provided function is a arrow function that takes each num in numbers and returns its double value (num * 2). The doubler function then returns the new array created by map, which contains all the doubled numbers.
